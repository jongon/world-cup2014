	public $components = array('Paginator', 'Session');

/**
 * Primer método que se ejecuta en el controlador de Usuarios
 * @return void
 */
	public function beforeFilter(){

		parent::beforeFilter();
		$this->Auth->allow('add');
	}

	public function isAuthorized($user){
		
		parent::isAuthorized($user);

		if ($user['role'] == User::ADMIN || $this->action == 'login')
			return true;
		else {
			//$this->redirect(array('controller' => 'bets', 'action' => 'add'));
			return false;
		}
	}

    public function login() {
       
        //if already logged-in, redirect
        if($this->Session->check('Auth.User')){
            $this->redirect(array('controller'=>'bets','action' => 'add'));      
        }

        if ($this->request->is('post')) {

            if ($this->Auth->login()) {

                $this->redirect($this->Auth->redirectUrl());

            } else {
                $this->Session->setFlash(__('Nombre de Usuario o contraseña incorrectos'),'default',array('class'=>'alert alert-danger'));
            }
        }        
    }
    
    public function logout() {

        $this->redirect($this->Auth->logout());
    }

/**
 * index method
 *
 * @return void
 */
	public function index() {
		$this->User->recursive = 0;
		$this->set('users', $this->Paginator->paginate());
	}

/**
 * view method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function view($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
		$this->set('user', $this->User->find('first', $options));
	}

	public function usersRankings(){

		$users = $this->User->find('all',array('order'=>(array('points'=>'desc'))));
		$this->set('users',$users);
	}

/**
 * add method
 *
 * @return void
 */
	public function add() {
		if ($this->request->is('post')) {
			$this->User->create();
			$this->request->data['User']['role'] = 'regular';
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'), 'default', array('class' => 'alert alert-success'));
				return $this->redirect(array('action' => 'login'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'), 'default', array('class' => 'alert alert-danger'));
			}
		}
	}

